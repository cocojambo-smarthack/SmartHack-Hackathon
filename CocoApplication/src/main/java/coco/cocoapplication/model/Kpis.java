package coco.cocoapplication.model;

public class Kpis {
    private int day;
    private long cost;
    private long co2;

    // Getter and setter for day
    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    // Getter and setter for cost
    public long getCost() {
        return cost;
    }

    public void setCost(long cost) {
        this.cost = cost;
    }

    // Getter and setter for co2
    public long getCo2() {
        return co2;
    }

    public void setCo2(long co2) {
        this.co2 = co2;
    }
}